    json_str = match.group(1)
    data = json.loads(json_str)
    
    for item in data:
        cid = item["id"]
        if cid in meanings_db:
            item["meanings"] = meanings_db[cid]
            
    # Keep the formatting beautiful
    new_json_str = json.dumps(data, ensure_ascii=False, indent=2)
    return "const tarotDataKo = " + new_json_str + ";"

